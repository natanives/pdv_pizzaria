package model;

public class ProdutoGenerico {
	private int id;
	private String nome;
	private double valor;
	
	public int getID() {
		return id;
	}
	
	public void setID(int id) {
		this.id = id;
	}

	public String getNome() {

		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public double getValor() {

		return valor;
	}

	public void setPreco(double valor) {
		this.valor = valor;
	}
}
